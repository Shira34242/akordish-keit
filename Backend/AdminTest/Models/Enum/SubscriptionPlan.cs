namespace AkordishKeit.Models.Enum;

/// <summary>
/// סוגי תוכניות מנוי
/// </summary>
public enum SubscriptionPlan
{
    /// <summary>
    /// חשבון חינמי - ללא קידום
    /// </summary>
    Free = 0,

    /// <summary>
    /// מנוי רגיל - 49 ₪/חודש
    /// כולל: 1 פרופיל מקצועי + תג מומלץ, קדימות בחיפוש, דף הבית, גלריה, וידאו, המלצות, שעות פעילות
    /// </summary>
    Regular = 1,

    /// <summary>
    /// מנוי פרימיום - 99 ₪/חודש (עוגן תמחורי)
    /// כולל: 1 פרופיל מקצועי + כל מה שבמנוי רגיל + יותר שטח נראות, יותר הדגשה עיצובית
    /// מיועד בעיקר להשוואה, לא למכירה בפועל
    /// </summary>
    Premium = 2,

    /// <summary>
    /// פרופיל מקצועי נוסף - 30 ₪/חודש (Add-on)
    /// מתווסף למנוי קיים (Regular או Premium)
    /// דוגמה: מורה + אולפן הקלטות, אמן + בעל מקצוע
    /// </summary>
    ExtraProfile = 3
}
